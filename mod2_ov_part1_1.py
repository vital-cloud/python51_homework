num1 = int(input("Ведите первое число: "))
num2 = int(input("Ведите второе число: "))
num3 = int(input("Ведите третье число: "))
func = input("Выберите одно из предложенных действий: \n"
            "*Сумма трех чисел, (нажмите '+') \n"
            "*Произведение двух чисел, (нажмите '*')\n")
if func == "+":
    print("Сумма чисел равна: ", num1 + num2 + num3,"\n")
elif func == "*":
    print("Произведение чисел равно: ", num1 * num2 * num3,"\n")
else:
    print("Выбранное действие не совпадает с условием!\n")

