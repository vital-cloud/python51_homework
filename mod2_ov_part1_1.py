num_1 = int(input("Ведите первое число: "))
num_2 = int(input("Ведите второе число: "))
num_3 = int(input("Ведите третье число: "))
num_tbl = [num_1,num_2,num_3]
func = input("Выберите одно из предложенных действий: \n"
            "*Вывести максимум трех чисел, (нажмите '>') \n"
            "*Вывести минимум трех чисел, (нажмите '<') \n"
            "*Вывести среднее арифметическое трех чисел, (нажмите '+-')\n")
if func == ">":
    print("Максимальное число: ", max(num_tbl),"\n")
elif func == "<":
    print("Минимальное число: ", min(num_tbl),"\n")
elif func == "+-":
    print("Среднее арифметическое чисел равно: ", (num_1 + num_2 + num_3) / 3,"\n")
else:
    print("Выбранное действие не совпадает с условием!\n")

